class Profesor:
    def __init__(self, id_profesor, nombre, a_paterno, a_materno, genero,
                 nombre_categoria, nombre_grado, num_trabajador,
                 rfc, curp, ingreso_unam, ingreso_carrera,
                 correo, num_cel, num_tel, direccion):
        self.id_profesor = id_profesor
        self.nombre = nombre
        self.a_paterno = a_paterno
        self.a_materno = a_materno
        self.genero = genero
        self.categoria = nombre_categoria
        self.grado = nombre_grado
        self.num_trabajador = num_trabajador
        self.rfc = rfc
        self.curp = curp
        self.ingreso_unam = ingreso_unam
        self.ingreso_carrera = ingreso_carrera
        self.correo = correo
        self.num_cel = num_cel
        self.num_tel = num_tel
        self.direccion = direccion
